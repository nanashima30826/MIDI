 int Result::m_ResultBGMNo = 0;
 Sound* Result::m_sound = nullptr;


//=============================================================================
// 初期化処理
//=============================================================================
void Result::Init()
{
	m_sound = new Sound;
	m_sound->Load((char*)"data/BGM/bgm2.wav");
	m_sound->SetVolume(1.0f);
	m_sound->Play(-1);
	
	ResultBG::InitResultBG();
}

//=============================================================================
// 終了処理
//=============================================================================
void Result::Uninit()
{

	ResultBG::UninitResultBG();
	m_sound->Uninit();
	delete m_sound;
}

//=============================================================================
// 更新処理
//=============================================================================
void Result::Update()
{
	ResultBG::UpdateResultBG();

	if (GetKeyboardTrigger(DIK_RETURN))
	{
		Fade::SetFade(MODE_FADE::FADE_OUT);
	}
	
}

//=============================================================================
// 描画処理
//=============================================================================
void Result::Draw()
{
	ResultBG::DrawResultBG();
}

