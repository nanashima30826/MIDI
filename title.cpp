int Title::m_TitleBGMNo = 0;
Sound* Title::m_sound=nullptr;

//=============================================================================
// 初期化処理
//=============================================================================
void Title::Init()
{
	m_sound = new Sound;
	m_sound->Load((char*)"data/BGM/bgm2.wav");
	m_sound->SetVolume(1.0f);
	m_sound->Play(-1);
	TitleBG::InitTitleBG();
	
	
}

//=============================================================================
// 終了処理
//=============================================================================
void Title::Uninit()
{
	TitleBG::UninitTitleBG();
	m_sound->Uninit();
	delete m_sound;
}

//=============================================================================
// 更新処理
//=============================================================================
void Title::Update()
{
	
	TitleBG::UpdateTitleBG();
	

	//シーン切り替え
	if (GetKeyboardTrigger(DIK_RETURN))
	{
		Fade::SetFade(MODE_FADE::FADE_OUT);
	}

}

//=============================================================================
// 描画処理
//=============================================================================
void Title::Draw()
{
	TitleBG::DrawTitleBG();
}

